# Here we learn about for loop in python. Now in this programe we use for loop for printing list items
userName=["Umer","Adil","Rehman"]
for i in userName:
    print(i)
else:
    print("For loop is completed")          
# Looping through String Values
x="Hello"
for i in x:
    print(i)
else:
    print("For loop is completed")    
# Range Function in For Loops
for i in range(10):
    print(i)
else:
    print("For loop is completed")     
for i in range(0,10):
    print(i)
else:
    print("For loop is completed")  
# Here value print 4 to 20 but there is addition of 2 in each value .
for i in range(2,10,2):
    print(i)
else:
    print("For loop is completed")               