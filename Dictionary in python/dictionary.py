# We learn how to create Dictionary in python
myD={1:"Red",2:"Green",3:"Blue"}
print(myD)
# We can print each value seperately by using key "1.."
print(myD[1])


# For adding data into our Dictionary afte creating
myD[4]="Yellow"
print(myD)


# For chaging the existing key value
myD[4]="Purple"
print(myD)


# For checking the lenght of Dictionary
print(len(myD))