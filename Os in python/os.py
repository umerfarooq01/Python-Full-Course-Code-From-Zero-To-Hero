# Get Current Working Directory with OS getdir() function
import os
print(os.getcwd())
# Get listdirs() in python using os module
print(os.listdir())
# Create Directory with OS makedirs() function
print(os.makedirs("PYTHON"))
# Check if File Exists with isfile() function
print(os.path.isfile("tuple.py"))
# Deleting a Directory in  Python
print(os.removedirs("PYTHON"))